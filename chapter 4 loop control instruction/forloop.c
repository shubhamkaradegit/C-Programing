#include <stdio.h>
int main()
{
    // for loop
    for (int i = 1; i <= 5; i = i + 1)
    {
        printf("hello world \n");
    }
    // number from 1 to 50
    for (int j = 1; j <= 50; j = j + 1)

    {
        printf("%d \n", j);
    }
    // number from 10 to 1
    for (int j = 10; j >= 1; j = j - 1) // i iterator counter variable

    {
        printf("%d \n", j);
    }


    
    return 0;
}
